int registro() //Função responsável por cadastrar os usuários no sistema
{
	//Inicio criação de variáveis/string
    char arquivo[40];
    char cpf[40];
    char nome[40];
    char sobrenome[40];
    char cargo[40];
    //Final da criação de variáveis/string
    
    printf("Digite o CPF a ser cadastrado: "); //Coletando informação do usuário
    scanf("%s", cpf); //%s refere-se a string
    
    strcpy(arquivo, cpf); //Responsável por copiar os valores das string
    
    FILE *file; //Cria o arquivo
    file = fopen(arquivo, "w"); //Cria o arquivo e o w significa escrever
    fprintf(file,cpf); //Salva o valor da variavel
    fclose(file); //Fecha o arquivo
    
    file = fopen(arquivo, "a");  //Abre o arquivo
    fprintf(file,",");
    fclose(file); //Fecha o arquivo
    
    printf("Digite o nome a ser cadastrado: "); //Cadastrando o nome do usuário
    scanf("%s",nome); //Refere-se a string
    
    file = fopen(arquivo, "a"); //Abre o arquivo
    fprintf(file,nome); 
    fclose(file); //Fecha o arquivo
    
    file = fopen(arquivo, "a"); //Abre o arquivo
    fprintf(file,",");
    fclose(file); //Fecha o arquivo
    
    printf("Digite o sobrenome a ser cadastrado: ");  //Cadastrando sobrenome do usuário
    scanf("%s",sobrenome);
    
    file = fopen(arquivo, "a"); //Abre o arquivo
    fprintf(file,sobrenome);
    fclose(file); //Fecha o arquivo
    
    file = fopen(arquivo, "a"); //Abre o arquivo
    fprintf(file,",");
    fclose(file); //Fecha o arquivo
    
    printf("Digite o cargo a ser cadastrado: ");  //Cadastrando o cargo do usuário
    scanf("%s",cargo);
    
    file = fopen(arquivo, "a"); //Abre o arquivo
    fprintf(file,cargo);
    fclose(file); //Fecha o arquivo
    
    system("pause");
    
}

int consulta()
{
	setlocale(LC_ALL, "Portuguese"); //Definindo a linguagem
	
	char cpf[40];
	char conteudo[200];
	
	printf("Digite o CPF a ser consultado: ");
	scanf("%s",cpf);
	
	FILE *file; //Abrir o arquivo (file)
	file = fopen(cpf,"r");
	
	if(file == NULL) //Se o arquivo for nulo
	{
		printf("Não foi possivel abrir o arquivo, não localizado!.\n");
	}
	
	while(fgets(conteudo, 200, file) != NULL)
	{
		printf("\nEssas são as informações do usuário: ");
		printf("%s", conteudo);
		printf("\n\n");
	}
	
	system("pause");
	fclose(file);
	
}

int deletar()
{
	char cpf [40];
	
	printf("Digite o CPF do usuário a ser deletado: ");
	scanf("%s",cpf);
	
	FILE *file;
	file = fopen(cpf,"r");
	
	if(file == NULL) //Se o usuário não for encontrado no sistema
	{
		printf("O usuário não se encontra no sistema!.\n");
		system("pause");
	}
	
	else
	{
		fclose(file);
		
		if (remove(cpf) == 0) //Tenta remover o arquivo correspondente ao CPF
		{
			printf("\nUsuário deletado com sucesso!\n\n");
			system("pause");
		}
		
		else
		{
			printf("\nErro ao deletar o usuário.\n\n");
			system("pause");
		}
	}
}



